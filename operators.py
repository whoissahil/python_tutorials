# operators in python:

# 1.) Arithmetic operators

print("5 + 6 is", 5 + 6)
print("5 - 6 is", 5 - 6)
print("5 * 6 is", 5 * 6)
print("5 / 6 is", 5 / 6)
print("5 ** 6 is", 5 ** 6)
print("5 % 6 is", 5 % 6)
print("15 // 6 is", 15 // 6)

# 2.) Assignment operators

x = 5
print(x)
x -= 7
print(x)
x /= 7
print(x)
x *= 7
print(x)

# 3.) Comparison operators

i = 5
print(i == 4)
print(i != 4)
print(i > 4)
print(i < 4)
print(i >= 4)
print(i <= 4)

# 4.) Logical operators

a = True
b = False

print(a and b)
print(a or b)

# 5.) Identity operators

print(a is b)
print(a is not b)

# 6.) Membership operators

list1 = [3, 3, 2, 2, 39, 33, 35, 32]
print(32 in list1)
print(99 in list1)
print(99 not in list1)
print(32 not in list1)

# 7.) Bitwise operators

# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11

print(0 & 1)
print(0 | 1)